    private void obtenerCurso(){
        ResultSet resultado = control.obtenerCurso();
        try {
            if (resultado != null) {
                while (resultado.next()) {                    
                    cCurso.addItem(resultado.getString("curso"));
                }
            }
        } catch (SQLException e) {
            System.out.println("Fallo obtenerCurso()");
        }
    }
   
    
