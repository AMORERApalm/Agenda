    private void consultarEstadisticas(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarEstadisticas
        modelo.setColumnCount(0);
        modelo.setRowCount(0);
        
        ResultSet asignaturas = control.obtenerAsignaturas(cCurso.getSelectedItem());  
        int numAlumnos = control.numAlumnos(cCurso.getSelectedItem());
        int numAsignaturas = control.numAsignaturas(cCurso.getSelectedItem());
        try {
            // FORMATO TABLA
            if (asignaturas != null) {
                modelo.addColumn("    ");
                modelo.addRow(new Object[]{"Insuficiente"});
                modelo.addRow(new Object[]{"Suficiente"});
                modelo.addRow(new Object[]{"Bien"});
                modelo.addRow(new Object[]{"Notable"});
                modelo.addRow(new Object[]{"Sobresaliente"});
                while (asignaturas.next()) {                
                    modelo.addColumn(asignaturas.getString("asignatura"));
                }
            }
            // CONTENIDO
            for (int i = 0; i < numAsignaturas; i++) {
                int insuficiente = 0,suficiente = 0,bien = 0,notable = 0,sobresaliente = 0;
                ResultSet notas = control.notasAsignatura(cEvaluacion.getSelectedItem(), modelo.getColumnName(i+1));
                if (notas != null) {
                    while (notas.next()) {                    
                        if (notas.getInt("nota") < 5) {
                            insuficiente++;
                        } else if(notas.getInt("nota") == 5){
                            suficiente++;
                        } else if(notas.getInt("nota") == 6){
                            bien++;
                        } else if(notas.getInt("nota") == 7 || notas.getInt("nota") == 8){
                            notable++;
                        } else if(notas.getInt("nota") == 9 || notas.getInt("nota") == 10){
                            sobresaliente++;
                        }
                    }
                }
                modelo.setValueAt(""+(float)(insuficiente*100/numAlumnos)+" %", 0, i+1);
                modelo.setValueAt(""+(float)(suficiente*100/numAlumnos)+" %", 1, i+1);
                modelo.setValueAt(""+(float)(bien*100/numAlumnos)+" %", 2, i+1);
                modelo.setValueAt(""+(float)(notable*100/numAlumnos)+" %", 3, i+1);
                modelo.setValueAt(""+(float)(sobresaliente*100/numAlumnos)+" %", 4, i+1);
            }
            
            
        } catch (SQLException e) {
            System.out.println("FALLO CREACION TABLA");
        }
    }//GEN-LAST:event_consultarEstadisticas

