    private void AplicarCambios(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AplicarCambios
        int rojo = scRojo.getValue();
        int verde = scVerde.getValue();
        int azul = scAzul.getValue();
        Color color = new Color(rojo,verde,azul);
        
        int aux = Integer.parseInt( cTamaño.getSelectedItem() );
        Font fuente = new Font("Dialog", Font.BOLD, aux);
        
        lResultado.setText(tTexto.getText());       
        lResultado.setFont(fuente);
        lResultado.setForeground(color);
        
        if (hilo == null) {
            hilo = new Thread(this);
            hilo.start();
        }
    }//GEN-LAST:event_AplicarCambios

