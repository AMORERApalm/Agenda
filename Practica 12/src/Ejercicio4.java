    public void parpadear() throws InterruptedException{
        for (int i = 0; i <= 1000; i++) {
            if (lResultado.isVisible() == true) {
                lResultado.setVisible(false);
            } else {
                lResultado.setVisible(true);
            }
            Thread.sleep(1000);
        }
    }
    
    public void run(){
        try {
            parpadear();
        } catch (InterruptedException e) {
        }
    }
    
