public class Curso {
    int idCurso;
    String curso;

    public Curso(int idCurso, String curso) {
        this.idCurso = idCurso;
        this.curso = curso;
    }

    public int getIdCurso() {
        return idCurso;
    }

    public String getCurso() {
        return curso;
    }
    
    
}
