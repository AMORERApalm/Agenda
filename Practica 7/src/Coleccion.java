    private void CrearPersona(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearPersona
        String nombre = tNombre.getText();
        String direccion = tDireccion.getText();
        int edad = 0;
        int telefono = 0;
        try {
            edad = Integer.parseInt( tEdad.getText() );
        } catch (java.lang.NumberFormatException e) {
            dVacio.setVisible(true);
            tEdad.requestFocus();
        }
        try {
            telefono = Integer.parseInt( tTelefono.getText() );
        } catch (java.lang.NumberFormatException e) {
            dVacio.setVisible(true);
            tTelefono.requestFocus();
        }
        if("".equals(tNombre.getText())){
            dVacio.setVisible(true);
            tNombre.requestFocus();
        }else{
            if("".equals(tDireccion.getText())){
                dVacio.setVisible(true);
                tDireccion.requestFocus();
            }else{
                if("".equals(tEdad.getText())){
                    tEdad.requestFocus();
                }else{
                    if("".equals(tTelefono.getText())){
                        tTelefono.requestFocus(); 
                    }else{
                        TPersona persona = new TPersona(nombre,direccion,edad,telefono);
        
                        // AÃ±ade al arrayList el Objeto
                        agendaContactos.add(persona);
                        // AÃ±ade el nombre atributo al choice
                        cContacto.add(persona.getNombre());

                        // Limpiado tras creacion
                        tNombre.setText(null);
                        tDireccion.setText(null);
                        tEdad.setText(null);
                        tTelefono.setText(null);
                        tNombre.requestFocus();
                    }
                }
            }              
        }
    }//GEN-LAST:event_CrearPersona

    private void MostrarColeccion(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_MostrarColeccion
        TPersona contacto = agendaContactos.get( cContacto.getSelectedIndex() );
        
        tNombre.setText( contacto.getNombre() );
        tNombre.requestFocus();
        tDireccion.setText( contacto.getDireccion() );
        tEdad.setText("" + contacto.getEdad() );
        tTelefono.setText("" + contacto.getTelefono() );
    }//GEN-LAST:event_MostrarColeccion

