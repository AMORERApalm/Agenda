    private void contadorPalabras(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_contadorPalabras
        String texto = taAnalizar.getText();
        int numPalabras = 0;
        System.out.println(texto);
        if ((String) cSeparador.getSelectedItem() == "Punto") {
            String[] resultado = texto.split("\\.");
            
            for (int i = 0; i < resultado.length; i++) {
                
                if (!resultado[i].isEmpty()) {
                    taResultado.append(""+resultado[i]+"\n");
                    numPalabras++;
                }else{
                    System.out.println("VALOR VACIO");
                }                
            }
            
            tPalabras.setText(""+numPalabras);
        } else if((String) cSeparador.getSelectedItem() == "Punto y Coma"){
            String[] resultado = texto.split(";");
            
            for (int i = 0; i < resultado.length; i++) {
                if (!resultado[i].isEmpty()) {
                    taResultado.append(""+resultado[i]+"\n");
                    numPalabras++;
                }else{
                    System.out.println("VALOR VACIO");
                }   
            }
            
            tPalabras.setText(""+numPalabras);
        }else if((String) cSeparador.getSelectedItem() == "Coma"){
            String[] resultado = texto.split(",");
            
            for (int i = 0; i < resultado.length; i++) {
                if (!resultado[i].isEmpty()) {
                    taResultado.append(""+resultado[i]+"\n");
                    numPalabras++;
                }else{
                    System.out.println("VALOR VACIO");
                }   
            }
            
            tPalabras.setText(""+numPalabras);
        }else if((String) cSeparador.getSelectedItem() == "Espacio en blanco"){
            String[] resultado = texto.split(" ");
            
            for (int i = 0; i < resultado.length; i++) {
                if (!resultado[i].isEmpty()) {
                    taResultado.append(""+resultado[i]+"\n");
                    numPalabras++;
                }else{
                    System.out.println("VALOR VACIO");
                }   
            }
            
            tPalabras.setText(""+numPalabras);
        }else{
            System.out.println("FALLO OBTENER SEPARADOR");
        }
    }//GEN-LAST:event_contadorPalabras

