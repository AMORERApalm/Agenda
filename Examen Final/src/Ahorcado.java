    private void jugar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jugar
        if (palabras.isEmpty()) {
            System.out.println("ARRAY DE PALABRAS VACIO");
        }else{
            tIntentos.setText("" + 5);
            tLetra.setText(null);
            tLetra.requestFocus();             
                
            int numAux = (int) ( palabras.size() * Math.random() );
            String palabraAux = (String) palabras.get(numAux);
            System.out.println(palabraAux);
            String palabraEnc="";
            for (int i = 0; i < palabraAux.length(); i++) {
                palabraEnc = palabraEnc+"_";
            }
            tPalabra.setText(palabraEnc);
            
        }
        
    }//GEN-LAST:event_jugar

