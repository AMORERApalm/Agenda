    private void añadirPalabras(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_añadirPalabras
        tNuevaPalabra.requestFocus();
        palabras.add(tNuevaPalabra.getText());
        for (int i = 0; i < palabras.size(); i++) {
            System.out.println(palabras.get(i));            
        }
        tNuevaPalabra.setText(null);
    }//GEN-LAST:event_añadirPalabras

